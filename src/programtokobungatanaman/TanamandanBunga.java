package programtokobungatanaman;

/**
 *
 * @author rizwa
 */
public class TanamandanBunga {
    String pemesan;
    String alamat;
    String jenistanaman;
    int jumlahtanaman;

    public String getPemesan() {
        return pemesan;
    }

    public void setPemesan(String pemesan) {
        this.pemesan = pemesan;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getJenistanaman() {
        return jenistanaman;
    }

    public void setJenistanaman(String jenistanaman) {
        this.jenistanaman = jenistanaman;
    }

    public int getJumlahtanaman() {
        return jumlahtanaman;
    }

    public void setJumlahtanaman(int jumlahtanaman) {
        this.jumlahtanaman = jumlahtanaman;
    }
    
    
}



